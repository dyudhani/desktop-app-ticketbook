    private void kolompasswordFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_kolompasswordFocusLost
        // jika kolom password sama dengan "password" atau kosong
        // kita akan menyetel teks "password" 

        // mengambil teks password
        String pass = String.valueOf(kolompassword.getPassword());

        if(pass.trim().equals("") || pass.trim().toLowerCase().equals("password"))
        {
            kolompassword.setText("password");
            kolompassword.setForeground(new Color(153,153,153));
        }

        // hapus garis perbatasan
        iconpassword.setBorder(null);
    }//GEN-LAST:event_kolompasswordFocusLost

    private void kolompasswordFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_kolompasswordFocusGained

        // mengambil teks password
        String pass = String.valueOf(kolompassword.getPassword());

        if(pass.trim().toLowerCase().equals("password"))
        {
            kolompassword.setText("");
            kolompassword.setForeground(Color.black);
        }

        // set garis batas hitam ke ikon password
        Border icon = BorderFactory.createMatteBorder(1, 1, 1, 1, Color.black);
        iconpassword.setBorder(icon);
    }//GEN-LAST:event_kolompasswordFocusGained

