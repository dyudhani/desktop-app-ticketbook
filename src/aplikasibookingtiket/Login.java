    private void tombolloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolloginActionPerformed

        PreparedStatement st;
        ResultSet rs;

        // mengambil username dan password
        String username = kolomusername.getText();
        String password = String.valueOf(kolompassword.getPassword());

        //query select untuk memeriksa apakah nama pengguna dan kata sandi ada di database
        String query = "SELECT * FROM tb_users WHERE username = ? AND PASSWORD = ?";

        //tunjukkan pesan jika username atau bidang password kosong
        if(username.trim().equals("username"))
        {
            JOptionPane.showMessageDialog(null, "Enter Your Username", "Empty Username", 2);

        }
        else if(password.trim().equals("password"))
        {
            JOptionPane.showMessageDialog(null, "Enter Your Password", "Empty Password", 2);
        }
        else{
            try {
                //memanggil class javatomysql
                st = javatomysql.getConnection().prepareStatement(query);
                // memanggil inputan username dan password
                st.setString(1, username);
                st.setString(2, password);
                rs = st.executeQuery();

                if(rs.next()){
                    // jika login berhasil, maka akan menuju ke form menu
                    MoviesForm mf = new MoviesForm();
                    mf.setVisible(true);
                    mf.pack();
                    mf.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                    this.dispose();
                }
                else{
                    // pesan error
                    JOptionPane.showMessageDialog(null, "Invalid Username or Password", "Login Error", 2);
                }

            } catch (SQLException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_tombolloginActionPerformed

