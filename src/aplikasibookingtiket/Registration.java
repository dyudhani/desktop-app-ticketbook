    // buat fungsi untuk memverifikasi kolom kosong
    public boolean validasikolom()
    {   // mengambil data
        String fname = kolomfullname.getText();
        String uname = kolomusername.getText();
        String phone = kolomphone.getText();
        String pass = String.valueOf(kolompassword.getPassword());
        String confirm = String.valueOf(kolomconfirmpassword.getPassword());
        
        // periksa kolom kosong
        if(fname.trim().equals("") || uname.trim().equals("") || phone.trim().equals("") 
            || pass.trim().equals("") || confirm.trim().equals(""))
        { // jika kosong
           JOptionPane.showMessageDialog(null, "One Or More Field Are Empty","Empty Fields",2);
           return false;
        }
        
        // periksa apakah data password dan confirm password itu sama
        // jika salah
        else if(!pass.equals(confirm))
        {
            JOptionPane.showMessageDialog(null, "Password Doesn't Match","Condirm Password",2);
            return false;
        }
        
        // jika benar
        else{
            return true;
        }
    }
    
    // buat fungsi untuk memeriksa apakah nama pengguna yang dimasukkan sudah ada di database
    public boolean checkusername(String username){
        
        PreparedStatement st;
        ResultSet rs;
        boolean username_exist = false;
        
        String query = "SELECT * FROM tb_users WHERE username = ?";
        
        try {
            // memanggil class javatomysql
            st = javatomysql.getConnection().prepareStatement(query);
            st.setString(1, username);
            rs = st.executeQuery();
            
            // jika akun sudah ada dalam database
            if(rs.next())
            {
                username_exist = true;
                // pesan error
                JOptionPane.showMessageDialog(null, "This User is Already Taken, Choose Another One", "Username Failed", 2);  
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Registration.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return username_exist;
        
    }
    
    
