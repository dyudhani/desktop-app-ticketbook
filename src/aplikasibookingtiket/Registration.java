    private void tombolregisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolregisterActionPerformed
        // mengambil data
        String fname = kolomfullname.getText();
        String username = kolomusername.getText();
        String pass = String.valueOf(kolompassword.getPassword());
        String confirm = String.valueOf(kolomconfirmpassword.getPassword());
        String phone = kolomphone.getText();
        String gender = "Male";

        if(tombolfemale.isSelected()){
            gender = "Female";
        }

        // periksa apakah datanya kosong
        if(validasikolom())
        {
            // periksa apakah username sudah ada dalam database
            if(!checkusername(username))
            {
                PreparedStatement ps;
                ResultSet rs;
                String register_user_query = "INSERT INTO tb_users (full_name, username, password, phone, gender) VALUES (?,?,?,?,?)";

                try {
                    // memanggil class javatomysql
                    ps = javatomysql.getConnection().prepareStatement(register_user_query);
                    // mengambil inputan
                    ps.setString(1, fname);
                    ps.setString(2, username);
                    ps.setString(3, pass);
                    ps.setString(4, phone);
                    ps.setString(5, gender);

                    if(ps.executeUpdate()!=0){
                        // pesan sukses
                        JOptionPane.showMessageDialog(null, "Your Account Has Been Created");
                    }
                    else{
                        // pesan error
                        JOptionPane.showMessageDialog(null, "Error : Check Your Information");
                    }

                } catch (SQLException ex) {
                    Logger.getLogger(Registration.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_tombolregisterActionPerformed

